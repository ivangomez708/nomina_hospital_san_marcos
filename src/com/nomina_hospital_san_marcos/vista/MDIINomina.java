    private void btnIngresarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnIngresarActionPerformed
        
        
        
        
        if (txtCorreo.getText().equals("") || txtContraseña.getText().equals(""))
        {
        JOptionPane.showMessageDialog(rootPane, "Debe digitar los campos obligatorios", "Error", 0);
        }
        else
        {
            if(controlUsuarios.verificarUsuario(txtCorreo.getText(), txtContraseña.getText()))
                
            {
            controlarEstadoInicio(true);
            JIFLogin.hide();
            }
            else
            {
            JOptionPane.showMessageDialog(rootPane, "Los datos ingresados son errados", "Error", 0);
            }
        }
    }//GEN-LAST:event_btnIngresarActionPerformed

