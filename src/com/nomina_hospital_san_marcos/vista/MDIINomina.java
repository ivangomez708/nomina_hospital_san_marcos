    private void llenarTrabajadores()
    {
        DefaultTableModel model = (DefaultTableModel) TBLTrabajadores.getModel();
        model.getDataVector().removeAllElements();
        //Object[] fila = {"3546584", "Ivan Gomez", "3000000", false};
        
        //model.addRow(fila);
        for(Trabajador trab: controlNomina.getTrabadores())
        {
            if(trab!=null)
            {
            Object[] fila= {trab.getCedula(), trab.getNombre(), trab.getSalario(), trab.isLicencia()};
            model.addRow(fila);
            }
        }

        TBLTrabajadores.repaint();
                
    }
    
